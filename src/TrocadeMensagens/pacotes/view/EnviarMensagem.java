    public void listarUsuarios() throws SQLException{
        UsuarioDAO dao = new UsuarioDAO();
        ArrayList<Usuarios> array = new ArrayList();
        array = dao.SelecionarTodos();
        
        for (Usuarios u: array){
            listaUsuarios.addItem(u.getUsername());
        }
    }

