    public Usuarios selectUsuarioByUsername(String username) throws SQLException{
        UsuarioDAO dao = new UsuarioDAO();
        ArrayList<Usuarios> array = dao.SelecionarTodos();
        Usuarios destinatario = new Usuarios();
        
        for(Usuarios u: array){
            if(u.getUsername().equals(username)){
               destinatario = u;
            }
        }
        return destinatario;
        
    }
