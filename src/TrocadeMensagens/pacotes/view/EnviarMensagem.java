    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        Login l = new Login();
        Usuarios remetente = l.usuarioLogado();
        String user_destinatario = (String) listaUsuarios.getSelectedItem();
        Usuarios destinatario = new Usuarios();
        try {
            destinatario = this.selectUsuarioByUsername(user_destinatario);
        } catch (SQLException ex) {
            Logger.getLogger(EnviarMensagem.class.getName()).log(Level.SEVERE, null, ex);
        }
      
        String corpo = txtMsg.getText().trim();
        Mensagens msg = new Mensagens(corpo, remetente.getId(), destinatario.getId());
        
        MensagensDAO dao = new MensagensDAO();
        try {
            dao.Adicionar(msg);
            JOptionPane.showMessageDialog(this, "Mensagem Enviada!");
            limparCampos();
        } catch (SQLException ex) {
            Logger.getLogger(EnviarMensagem.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

